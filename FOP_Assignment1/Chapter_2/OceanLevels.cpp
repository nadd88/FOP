#include <iostream>
using namespace std;

int main(){
    double oceanRise = 1.5;
    double oceanRiseIn5years = 5 * oceanRise;
    double oceanRiseIn7years = 7 * oceanRise;
    double oceanRiseIn10years = 10 * oceanRise;
    
    cout << "Ocean rise in 1 year is: " << oceanRise << endl;
    cout << "Ocean rise in 5 years is: " << oceanRiseIn5years << endl;
    cout << "Ocean rise in 7 years is: " << oceanRiseIn7years << endl;
    cout << "Ocean rise in 10 years is: " << oceanRiseIn10years << endl;
}